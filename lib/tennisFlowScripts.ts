/** 指定したコート数で、各 step に対応するセリフを返す */
export function getScriptsForCourt(
  mode: "single" | "double"
): FlowScript[] {
  return TENNIS_FLOW_SCRIPTS.filter(
    (s) => !s.courtMode || s.courtMode === "both" || s.courtMode === mode
  ).sort((a, b) => a.step - b.step);
}

/** 指定した step 番号 + コート数に対応するセリフを返す */
export function findScript(
  step: number,
  mode: "single" | "double"
): FlowScript | undefined {
  return getScriptsForCourt(mode).find((s) => s.step === step);
}

/** 長さ設定に応じて読み上げ用テキストを選ぶ(指定が無ければ通常へ自動フォールバック) */
export function pickVoiceText(
  script: FlowScript,
  length: VoiceLength
): string {
  if (length === "veryShort" && script.voiceTextVeryShort) {
    return script.voiceTextVeryShort;
  }
  if (length === "short" && script.voiceTextShort) {
    return script.voiceTextShort;
  }
  return script.voiceText;
}

