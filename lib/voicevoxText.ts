/** 句読点・空白の最終整理 */
function cleanupPunctuation(text: string): string {
  return (
    text
      // 改行 → 。
      .replace(/\s*[\r\n]+\s*/g, "。")
      // ！/! → 。
      .replace(/[！!]/g, "。")
      // 連続する句点を一つに
      .replace(/。+/g, "。")
      // 連続する空白を一つに
      .replace(/\s+/g, " ")
      .trim()
      // 文末に句点がなければ追加
      .replace(/([^。！？!?])$/, "$1。")
  );
}

/**
 * VOICEVOX に渡す直前に呼ぶ補正関数。
 *
 * - 表示テキスト(displayText)には影響しません
 * - コピーされるテキストにも影響しません
 * - VOICEVOX ENGINE に送る文字列だけが変わります
 */
export function sanitizeForVoicevox(text: string): string {
  let result = text;

  // 1. URL 除去
  result = removeUrls(result);

  // 2. 絵文字除去
  result = removeEmoji(result);

  // 3. 語彙・読み補正
  for (const [pattern, replacement] of REPLACEMENTS) {
    result = result.replace(pattern, replacement);
  }

  // 4. 句読点・空白整理
  result = cleanupPunctuation(result);

  if (process.env.NODE_ENV === "development") {
    // eslint-disable-next-line no-console
    console.log("[VOICEVOX sanitize] before:", text);
    // eslint-disable-next-line no-console
    console.log("[VOICEVOX sanitize] after: ", result);
  }

  return result;
}
